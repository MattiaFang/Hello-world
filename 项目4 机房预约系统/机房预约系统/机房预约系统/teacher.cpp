//查看所有预约
void Teacher::showAllOrder()
{
	OrderFile of;
	if(of.m_Size == 0)
	{
		cout<<"无预约记录"<<endl;
		system("pause");
		system("cls");
		return ;
	}
	for(int i=0;i<of.m_Size;i++)
	{
		cout<<i+1<<"、";
		cout<<"预约日期：周"<<of.m_orderData[i]["date"];
		cout<<"  时段："<<of.m_orderData[i]["interval"]=="1"?"上午":"下午";
		cout<<"  学号："<<of.m_orderData[i]["stuId"];
		cout<<"  姓名："<<of.m_orderData[i]["stuName"];
		cout<<"  机房："<<of.m_orderData[i]["roomId"];
		string status = "状态：";//0、取消的预约 1、审核中  2、已预约  -1、预约失败
		if(of.m_orderData[i]["status"]=="1")
		{
			status +="审核中";
		}
		if(of.m_orderData[i]["status"]=="2")
		{
			status +="预约成功";
		}
		if(of.m_orderData[i]["status"]=="-1")
		{
			status +="预约失败";
		}
		if(of.m_orderData[i]["status"]=="0")
		{
			status +="预约已取消";
		}
		cout<<status<<endl;
	}
	system("pause");
	system("cls");
	return;
}
//审核所有预约
void Teacher::validOrder()
{
	OrderFile of;
	if(of.m_Size == 0)
	{
		cout<<"无预约记录"<<endl;
		system("pause");
		system("cls");
		return ;
	}
	vector<int>v;
	int index = 0;
	for(int i=0;i<of.m_Size;i++)
	{
		if(of.m_orderData[i]["status"]=="1")
		{
			v.push_back(i);
			cout<<++index<<"、";
			cout<<"预约日期：周"<<of.m_orderData[i]["date"];
			cout<<"  时段："<<of.m_orderData[i]["interval"]=="1"?"上午":"下午";
			cout<<"  学号："<<of.m_orderData[i]["stuId"];
			cout<<"  姓名："<<of.m_orderData[i]["stuName"];
			cout<<"  机房："<<of.m_orderData[i]["roomId"];
			string status = "状态：审核中";
			cout<<status<<endl;
		}	
	}
	if(index==0)
	{
		cout<<"当前无预约记录需要审核"<<endl;
		system("pause");
		system("cls");
		return;
	}
	cout<<"请输入审核的预约记录，0表示返回"<<endl;
	int select = 0;
	int ret = 0;
	while(true)
	{
		cin>>select;
		if(select>=0 && select<=v.size())
		{
			if(select == 0)
			{
				break;
			}
			cout<<"请选择审核结果：(1、通过  2、不通过)"<<endl;
			cin>>ret;
			while(true)
			{
				if(ret==1)
				{
					of.m_orderData[v[select-1]]["status"] = "2";
					break;
				}
				else if(ret==2)
				{
					of.m_orderData[v[select-1]]["status"] = "-1";
					break;
				}
				else
				{
					cout<<"输入错误请重新输入"<<endl;
				}
			}
			of.updateOrder();//更新预约记录
			break;
		}
		else
		{
			cout<<"输入错误,请重新输入！"<<endl;
		}
	}
	system("pause");
	system("cls");
	return;
}
