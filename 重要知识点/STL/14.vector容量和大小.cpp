#include<iostream>
#include<vector>
using namespace std;
void printVector(vector<int>&v)
{
	for(vector<int>::iterator it=v.begin();it != v.end();it++)
	{
		cout<<*it<<" ";
	}
	cout<<endl;
}
//vector容量和大小
void test01()
{
	vector<int>v1;
	for(int i=0;i<10;i++)
	{
		v1.push_back(i);
	}
	printVector(v1);

	if(v1.empty())
		cout<<"v1为空"<<endl;
	else
	{	
		cout<<"v1不为空"<<endl;
		cout<<"v1的容量为："<< v1.capacity()<<endl;
		cout<<"v1的大小为："<< v1.size()<<endl;//容量大于等于大小
	}
	v1.resize(15);
	//v1.resize(15,100);//利用重载版本，可以指定默认填充值，参数2
	printVector(v1);//如果重新指定比原来长了，默认用0填充新的位置
	v1.resize(5);
	printVector(v1);//如果重新指定比原来短了，删除多出来的部分
}
int main()
{
	test01();
	system("pause");
	return 0;
}